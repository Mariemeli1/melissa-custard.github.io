// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
let count = 0;
for(let i = 0; i < str.length; i++){
  if(str[i] === char) {
    count += 1;
  }
}
return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
return countChars(str, "B");
}
console.log(countChars("BigBoyBusiness", "B"));
